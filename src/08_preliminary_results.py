# src/08_preliminary_results.py
#
# Step 8: Consolidate preliminary results into a single summary.
#
# This script does NOT rerun any analysis. It reads the output files created
# by steps 03 to 07 and produces:
#   - A consolidated preliminary_results.csv with one row per research question
#   - A pretty-printed text summary at outputs/tables/preliminary_results_summary.txt
#   - A "face validity" check for the immune checkpoint inhibitor pairs that
#     reproduces the sanity check described in the interim report
#
# Run this AFTER 03 through 07 have completed.

from pathlib import Path
import pandas as pd
import numpy as np

PROCESSED_DIR = Path("data/processed")
TABLE_DIR = Path("outputs/tables")
TABLE_DIR.mkdir(parents=True, exist_ok=True)


def load_csv_or_none(path):
    p = Path(path)
    if not p.exists():
        return None
    return pd.read_csv(p)


def section(title):
    line = "=" * len(title)
    return f"\n{title}\n{line}\n"


def main():
    summary_lines = []
    summary_lines.append("PRELIMINARY RESULTS SUMMARY")
    summary_lines.append("Drug Safety Surveillance: Building a Signal-Detection Engine on FDA FAERS")
    summary_lines.append("Generated by 08_preliminary_results.py")
    summary_lines.append("")

    # ---------- Pipeline counts ----------
    pipeline = load_csv_or_none(TABLE_DIR / "pipeline_stage_counts.csv")
    if pipeline is not None:
        summary_lines.append(section("Pipeline stage counts (Table 8 in interim report)"))
        summary_lines.append(pipeline.to_string(index=False))

    # ---------- Flag counts (Table 10) ----------
    flags = load_csv_or_none(TABLE_DIR / "flag_counts.csv")
    if flags is not None:
        summary_lines.append(section("Disproportionality flag counts (Table 10)"))
        summary_lines.append(flags.to_string(index=False))

    # ---------- Cross-method concordance ----------
    concordance = load_csv_or_none(TABLE_DIR / "rq2_method_concordance.csv")
    if concordance is not None:
        summary_lines.append(section("Cross-method Spearman concordance"))
        summary_lines.append(concordance.to_string(index=False))

    # ---------- RQ1 results ----------
    rq1 = load_csv_or_none(TABLE_DIR / "rq1_isolation_forest_results.csv")
    if rq1 is not None:
        summary_lines.append(section("RQ1 - Isolation Forest"))
        for col in rq1.columns:
            summary_lines.append(f"  {col}: {rq1[col].iloc[0]}")

    # ---------- RQ2 results ----------
    rq2 = load_csv_or_none(TABLE_DIR / "rq2_gmm_results.csv")
    if rq2 is not None:
        summary_lines.append(section("RQ2 - Gaussian Mixture Model"))
        for col in rq2.columns:
            summary_lines.append(f"  {col}: {rq2[col].iloc[0]}")

    # ---------- RQ3 results ----------
    rq3 = load_csv_or_none(TABLE_DIR / "rq3_random_forest_results.csv")
    if rq3 is not None:
        summary_lines.append(section("RQ3 - Random Forest vs OMOP"))
        for col in rq3.columns:
            summary_lines.append(f"  {col}: {rq3[col].iloc[0]}")

    # ---------- RQ4 results ----------
    rq4 = load_csv_or_none(TABLE_DIR / "rq4_survival_results.csv")
    if rq4 is not None:
        summary_lines.append(section("RQ4 - ICI Random Survival Forest"))
        for col in rq4.columns:
            summary_lines.append(f"  {col}: {rq4[col].iloc[0]}")

    # ---------- ICI face validity check ----------
    summary_lines.append(section("ICI face validity check"))
    cand_path = PROCESSED_DIR / "candidate_set.parquet"
    if cand_path.exists():
        cand = pd.read_parquet(cand_path)
        ici_pts = [
            "Hypothyroidism", "Pneumonitis", "Colitis", "Hepatitis",
            "Adrenal Insufficiency", "Myocarditis", "Hypophysitis",
        ]
        ici_drugs = ["PEMBROLIZUMAB", "NIVOLUMAB", "IPILIMUMAB"]
        face = cand[
            cand["ingredient"].isin(ici_drugs) & cand["pt"].isin(ici_pts)
        ][["ingredient", "pt", "a", "ror", "prr", "ic", "ebgm", "eb05",
           "flag_count", "flag_consensus"]]
        face = face.sort_values(["ingredient", "pt"])
        face.to_csv(TABLE_DIR / "ici_face_validity.csv", index=False)
        summary_lines.append(face.to_string(index=False))
    else:
        summary_lines.append("  candidate_set.parquet not found - run step 03 first.")

    # ---------- Hypothesis decision summary ----------
    summary_lines.append(section("Hypothesis decisions summary"))
    decisions = []
    for rq_num, results_df, key in [
        ("RQ1 (H1a)", rq1, "h1a_accepted"),
        ("RQ2 (H2a)", rq2, "h2a_accepted"),
        ("RQ3 (H3a)", rq3, "h3a_accepted"),
        ("RQ4 (H4a)", rq4, "h4a_accepted"),
    ]:
        if results_df is None or key not in results_df.columns:
            decisions.append({"research_question": rq_num, "decision": "not yet run"})
        else:
            accepted = bool(results_df[key].iloc[0])
            decisions.append({
                "research_question": rq_num,
                "decision": "ACCEPT alternative" if accepted else "DO NOT REJECT null"
            })

    decisions_df = pd.DataFrame(decisions)
    summary_lines.append(decisions_df.to_string(index=False))
    decisions_df.to_csv(TABLE_DIR / "hypothesis_decisions.csv", index=False)

    # ---------- Write summary file ----------
    out_path = TABLE_DIR / "preliminary_results_summary.txt"
    out_path.write_text("\n".join(summary_lines))
    print(f"\nWrote consolidated summary to {out_path}")
    print("\n" + "=" * 60)
    print("\n".join(summary_lines))


if __name__ == "__main__":
    main()
